let num1=Math.abs(-5.5)
let num2=Math.abs(10)
document.write(`${num1} <br>`)
document.write(`${num2} <br>`)
// math.abs trả về giá trị tuyệt đúi
let num3=Math.ceil(4.4)
let num4=Math.ceil(4.8)
document.write(`${num3} <br>`)
document.write(`${num4} <br>`)
// math.ceil luôn làm tròn lên trên số nguyên gần nhất
let num5=Math.floor(5.7)
let num6=Math.floor(6.1)
document.write(`${num5} <br>`)
document.write(`${num6} <br>`)
// math.floor luôn làm tròn xuống số nguyên gần nhất
let num7=Math.round(10.1)
let num8=Math.round(20.6)
document.write(`${num7} <br>`)
document.write(`${num8} <br>`)
// math.round làm tròn lên hoặc xuống tùy vào số nguyên nào gần nó hơn